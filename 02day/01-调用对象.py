class pig():
	def eat(self):
		print('猪饲料')
	def sleep(self):
		print('哼哼睡')
	def introduce(self):
		print('我是%s年龄%d颜色%s'%(self.name,self.age,self.color))
peiqi = pig()
peiqi.name = '我是小猪配齐'
peiqi.age = 10
peiqi.color = '粉色'
peiqi.eat()
peiqi.sleep()
peiqi.introduce()


